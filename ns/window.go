func applyFilterBankWindow(x *[fftSize]float32) {
	for i := 0; i < 96; i++ {
		x[i] = kBlocks160w256FirstHalf[i] * x[i]
	}
	for i, k := 161, 95; i < fftSize; i, k = i+1, k-1 {
		x[i] = kBlocks160w256FirstHalf[k] * x[i]
	}
	// x[96..160] inclusive are in the flat region — coefficient 1.0.
}
